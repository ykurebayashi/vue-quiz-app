<script setup>
  import{useRouter} from 'vue-router'

  const router = useRouter()
  const props = defineProps(['quizData'])

  const navigateToQuiz = () => {
    router.push(`/quiz/${props.quizData.id}`)
  }
</script>

<template>
    <div @click="navigateToQuiz" class="card">
        <img :src="quizData.img" :alt="quizData.name">
        <div class="card-text">
            <h2>{{ quizData.name }}</h2>
            <p>{{ quizData.questions.length }} questions</p>
        </div>
    </div>
</template>

